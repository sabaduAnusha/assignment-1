package com.java;

public class ProjectModal {
    int name;
    int id;
    int module;

    public ProjectModal(int name, int id, int module) {
        this.name = name;
        this.id = id;
        this.module = module;
    }

    public void setName(int name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setModule(int module) {
        this.module = module;
    }
}

